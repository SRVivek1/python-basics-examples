"""
    Unlike java and other typed programming languages in python a variable cam hold any type of value.

"""


# int
x = 1
print(f'type(x) : {type(x)}')

# str
x = 'hello'
print(f'type(x) : {type(x)}')

# bool
x = False
print(f'type(x) : {type(x)}')

# dict
x = {1: 'one', 2: 'two'}
print(f'type(x) : {type(x)}')

# list
x = [1, 2, 3]
print(f'type(x) : {type(x)}')

# none
x = None
print(f'type(x) : {type(x)}')


